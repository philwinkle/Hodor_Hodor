/**
 * Hodor_Hodor_Hodor_Hodor Hodor
 */
class Hodor_Hodor_Hodor_Hodor
{
    /**
     * Hodor hodor, hodor
     *
     * @param string $hodor
     *
     * @return string
     */
    public function hodor($hodor)
    {
        $Hodor = $this;
        return $Hodor->_hodor($hodor);
    }

    /**
     * Hodor hodor, hodor
     *
     * @param string $hodor
     *
     * @return string
     */
    protected function _hodor($hodor)
    {
        return preg_replace('{\w+}', 'Hodor', $hodor);
    }
}
